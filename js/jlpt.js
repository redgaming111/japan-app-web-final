function goJLPT(level) {
  if (!jlptData[level]) return;

  quizSection.style.display = "block";
  quizContainer.innerHTML = "";
  resultDiv.innerHTML = "";

  quizTitle.innerText = "Đề thử " + level;
  currentQuestions = jlptData[level];

  renderQuiz(currentQuestions);
  quizSection.scrollIntoView({ behavior: "smooth" });
}

function renderQuiz(questions) {
  questions.forEach((q, index) => {
    const div = document.createElement("div");
    div.classList.add("quiz-item");

    div.innerHTML = `
      <p class="quiz-question">Câu ${index + 1}: ${q.question}</p>
      <div class="options">
        ${q.options.map((opt, i) => `
          <label class="option">
            <input type="radio" name="q${index}" value="${i}">
            <span>${opt}</span>
          </label>
        `).join("")}
      </div>
      <div class="feedback" id="feedback-${index}"></div>
    `;

    quizContainer.appendChild(div);
  });
}

submitBtn.addEventListener("click", function () {
  if (currentQuestions.length === 0) return;

  let score = 0;

  currentQuestions.forEach((q, index) => {
    const selected = document.querySelector(`input[name="q${index}"]:checked`);
    const feedback = document.getElementById(`feedback-${index}`);
    const options = document.querySelectorAll(`input[name="q${index}"]`);

    options.forEach(opt => {
      opt.parentElement.classList.remove("correct", "wrong");
    });

    if (selected) {
      const userAnswer = parseInt(selected.value);

      if (userAnswer === q.answer) {
        score++;
        selected.parentElement.classList.add("correct");
        feedback.innerHTML = "✔ Đúng";
        feedback.className = "feedback correct-text";
      } else {
        selected.parentElement.classList.add("wrong");
        options[q.answer].parentElement.classList.add("correct");

        feedback.innerHTML =
          "✖ Sai. Đáp án đúng: <b>" +
          q.options[q.answer] +
          "</b>";
        feedback.className = "feedback wrong-text";
      }
    } else {
      feedback.innerHTML =
        "✖ Bạn chưa chọn. Đáp án đúng: <b>" +
        q.options[q.answer] +
        "</b>";
      feedback.className = "feedback wrong-text";
      options[q.answer].parentElement.classList.add("correct");
    }
  });

  resultDiv.innerHTML =
    "<h3>Kết quả: " +
    score +
    "/" +
    currentQuestions.length +
    " câu đúng</h3>";
});
